// For
console.log(`utilizando o For:`)

for (c = 1; c < 6; c++){
    console.log(`Repetindo por ${c} vezes`)
}
console.log(`\n`)

// While 
console.log(`utilizando o While:`)

var d = 1

while (d < 6){
    console.log(`Repetindo por ${d} vezes`)
    d++
}
console.log(`\n`)

// Do While
console.log(`utilizando o Do While:`)

var f = 1

do {
    console.log(`Repetindo por ${f} vezes`)
    f++
} while (f < 6)